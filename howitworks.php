<font size="+3" <p>Bihar Police Centralised Grievance Redressal Cell popularly known as "Bihar Police Helpline" is centralized contact for easier access to Bihar Police Grievance related systems.</p></font>
<center>
<img id="midimg"src="how.jpg" width="700" height="400">
</center>

<font size="+3" <p>This helpline caters as a central point which provides information related to the Bihar Police Helpline and providing platform to citizens for registering their grievance feedback or any information. This system also enables the Police Department and the citizen to have an access and control for online monitoring the grievance redress procedure from end-to-end.</p></font>
<font size="+3" <p>Once a complaint is registered in the system it directly goes to the Action Taking Authority for the redressal of the complaint. If the Authority doesn't take any action in the appropriate timeline consequently, it escalates to the next level for redressal.</p></font>




