<u><b><font color="Red" >Bihar Police: An Epitome of Excellence</font></b></u>
</div>
<div id="paragraph">
<font size="+3" <p>Policing in Bihar is more than 3000 years old. In fact, there are documented historical references to policing practices adopted by the Magadh Empire.</p></font>
<font size="+3" <p>
Policing in the Modern era in Bihar began in the year 1862 with the introduction of the Indian Police Act of 1861. Following the creation of the province of Bihar in 1912, the basic structure of police as it exists today in the whole of India was laid.There have been several officers of the Bihar province in the pre-independent India who have made pioneering contributions to policing in India.</p></font>
<font size="+3" <p>To name a few; Mr.Walter Swain of Swain Beat system fame, Shri AK Sinha, the first Indian to become an IGP of any province, Shri BN Mullick, the founder of the Intelligence Bureau and Deputy Superintendent of Police, Khan Bahadur Azizul Haque, credited with the primary development of the fingerprint classification system, the famous '1024 pigeon holes' cabinet system eventually named after his supervisor, Sir Edward Richard Henry leaps readily to the mind.</p></font> 
<font size="+3" <p>Post-independence, Bihar Police holds the legacy of a rich tradition with the introduction of innovative policing and welfare practices such as creation of a Police Welfare Fund, Police Hospitals and Police Information Room(PIR) in 1952. A Police Commission was also set up in 1958, whose mandate was to bring the police closer to the people. The Bihar Policemen's Association which came into existence in 1967 was the first of its kind to look after the interests of the policemen.
Today, Bihar police, with the cooperation of the people that it is mandated to serve and its rich traditions of excellence is firmly committed to confront and surmount the numerous challenges it faces.</p></font>
